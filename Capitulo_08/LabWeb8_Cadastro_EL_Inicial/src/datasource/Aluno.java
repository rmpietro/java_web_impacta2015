package datasource;

public class Aluno {

	private String nome;
	private String userName;
	private String email;
	private int idade;
	
	public Aluno(String nome, String userName, String email, int idade) {
		super();
		this.nome = nome;
		this.userName = userName;
		this.email = email;
		this.idade = idade;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public int getIdade() {
		return idade;
	}

	public void setIdade(int idade) {
		this.idade = idade;
	}
	
	
	
}
